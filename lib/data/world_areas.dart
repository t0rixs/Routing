/// 達成度タブで「あなたの踏破面積を世界の地物と比較する」ために使う
/// 参考データセット。面積は **平方キロメートル (km²)** で保持する。
///
/// - `nameJa` / `nameEn`: 表示名（ロケール別）。
/// - `iso2`: ISO 3166-1 alpha-2（kind=='country' のみ）。UI で国旗を描画する。
/// - `regionScope`: 'jp' などのスコープ。指定 Locale でのみ表示する（例: 都道府県）。
///
/// 出典: Wikipedia 各記事（2024 年時点の概算値）。
library;

class WorldArea {
  const WorldArea({
    required this.nameJa,
    required this.nameEn,
    required this.areaKm2,
    this.kind = 'country',
    this.iso2,
    this.regionScope,
  });

  final String nameJa;
  final String nameEn;

  /// 面積 (km²)
  final double areaKm2;

  /// 'country' | 'region' | 'landmark' | 'island' | 'natural'
  final String kind;

  /// ISO 3166-1 alpha-2（kind=='country' で国旗を出すときに使う）
  final String? iso2;

  /// 'jp' など。指定された場合、その languageCode の時のみリストに含める。
  final String? regionScope;

  /// Locale から表示名を返す。
  String displayName(String languageCode) =>
      languageCode == 'ja' ? nameJa : nameEn;
}

/// Locale に応じて表示すべき項目を返す（順序はマスタ順）。
